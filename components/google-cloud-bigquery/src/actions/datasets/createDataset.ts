import { action } from "@prismatic-io/spectral";
import { createClient } from "../../client";
import {
  access,
  connectionInput,
  creationTime,
  datasetReference,
  defaultCollation,
  defaultEncryptionConfiguration,
  defaultPartitionExpirationMs,
  defaultRoundingMode,
  defaultTableExpirationMs,
  description,
  etag,
  friendlyName,
  id,
  isCaseInsensitive,
  kind,
  labels,
  lastModifiedTime,
  location,
  maxTimeTravelHours,
  projectId,
  satisfiesPzs,
  selfLink,
  storageBillingModel,
  tags,
} from "../../inputs";

export const createDataset = action({
  display: {
    description: "Creates a new empty dataset.",
    label: "Create Dataset",
  },
  inputs: {
    connectionInput,
    projectId,
    datasetReference,
    kind,
    etag,
    id,
    selfLink,
    friendlyName,
    description,
    defaultTableExpirationMs,
    defaultPartitionExpirationMs,
    labels,
    access,
    creationTime,
    lastModifiedTime,
    location,
    defaultEncryptionConfiguration,
    satisfiesPzs,
    isCaseInsensitive,
    defaultCollation,
    defaultRoundingMode,
    maxTimeTravelHours,
    tags,
    storageBillingModel,
  },
  perform: async (
    _context,
    {
      connectionInput,
      projectId,
      kind,
      etag,
      id,
      selfLink,
      datasetReference,
      friendlyName,
      description,
      defaultTableExpirationMs,
      defaultPartitionExpirationMs,
      labels,
      access,
      creationTime,
      lastModifiedTime,
      location,
      defaultEncryptionConfiguration,
      satisfiesPzs,
      isCaseInsensitive,
      defaultCollation,
      defaultRoundingMode,
      maxTimeTravelHours,
      tags,
      storageBillingModel,
    },
  ) => {
    const client = createClient(connectionInput);
    const { data } = await client.datasets.insert({
      projectId: projectId || undefined,
      requestBody: {
        kind: kind || undefined,
        etag: etag || undefined,
        id: id || undefined,
        selfLink: selfLink || undefined,
        datasetReference: datasetReference || undefined,
        friendlyName: friendlyName || undefined,
        description: description || undefined,
        defaultTableExpirationMs: defaultTableExpirationMs || undefined,
        defaultPartitionExpirationMs: defaultPartitionExpirationMs || undefined,
        labels: labels || undefined,
        access: access || undefined,
        creationTime: creationTime || undefined,
        lastModifiedTime: lastModifiedTime || undefined,
        location: location || undefined,
        defaultEncryptionConfiguration:
          defaultEncryptionConfiguration || undefined,
        satisfiesPzs,
        isCaseInsensitive,
        defaultCollation: defaultCollation || undefined,
        defaultRoundingMode: defaultRoundingMode || undefined,
        maxTimeTravelHours: maxTimeTravelHours || undefined,
        tags: tags || undefined,
        storageBillingModel: storageBillingModel || undefined,
      },
    });
    return {
      data,
    };
  },
});
